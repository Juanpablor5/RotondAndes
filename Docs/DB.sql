CREATE TABLE REGISTRO(
   CODIGO INTEGER NOT NULL,
   USUARIO VARCHAR2(100) NOT NULL,
   CONTRASENIA VARCHAR2(100) NOT NULL,
   PERMISOS INTEGER NOT NULL,
   CONSTRAINT PK_REGISTRO PRIMARY KEY (CODIGO)
);

CREATE TABLE CLIENTEINFO(
  CEDULA INTEGER NOT NULL,
  NOMBRE VARCHAR(100) NOT NULL,
  FECHADEINGRESO DATE,
  REGISTRO_ID NOT NULL UNIQUE,
  CONSTRAINT FK_CLIENTE_REGISTRO FOREIGN KEY (REGISTRO_ID) REFERENCES REGISTRO(CODIGO),
  CONSTRAINT PK_CLIENTE PRIMARY KEY (CEDULA)
);

CREATE TABLE PREFERENCIAS(
  ID INTEGER NOT NULL,
  PRECIOMENOR NUMBER,
  PRECIOMAYOR NUMBER,
  CLIENTE_ID INTEGER NOT NULL,
  CONSTRAINT FK_PREFERENCIAS_CLIENTE FOREIGN KEY (CLIENTE_ID) REFERENCES CLIENTEINFO(CEDULA),
  CONSTRAINT PK_PREFERENCIAS PRIMARY KEY (ID)
);

CREATE TABLE PEDIDO(
   ID INTEGER NOT NULL,
   FECHA DATE,
   CLIENTE_ID INTEGER,
   CONSTRAINT FK_PEDIDO_CLIENTES FOREIGN KEY (CLIENTE_ID) REFERENCES CLIENTEINFO(CEDULA),
   CONSTRAINT PK_PEDIDO PRIMARY KEY (ID)
);

CREATE TABLE CATEGORIA(
  ID INTEGER NOT NULL,
  NOMBRE VARCHAR2(100 BYTE) NOT NULL,
  CONSTRAINT PK_CATEGORIA PRIMARY KEY (ID)
);

CREATE TABLE PREFERENCIASCATEGORIA(
   PREFERENCIAS_ID INTEGER NOT NULL,
   CATEGORIA_ID INTEGER NOT NULL,
   CONSTRAINT FK_PREFCATEGORIA_PREF FOREIGN KEY (PREFERENCIAS_ID) REFERENCES PREFERENCIAS(ID),
   CONSTRAINT FK_PREFCATEGORIA_CATEGORIA FOREIGN KEY (CATEGORIA_ID) REFERENCES CATEGORIA(ID),
   CONSTRAINT PK_PREFCATEGORIA PRIMARY KEY (PREFERENCIAS_ID,CATEGORIA_ID)
);

CREATE TABLE PRODUCTO(
  ID INTEGER NOT NULL,
  NOMBRE VARCHAR2(100 BYTE) NOT NULL,
  DESCRIPCION VARCHAR2(1000 BYTE)  NOT NULL,
  TRADUCCION VARCHAR2(1100 BYTE) NOT NULL,
  TIEMPOPREPARACION INTEGER NOT NULL,
  CATEGORIA_ID INTEGER NOT NULL,
  CHECK (CATEGORIA_ID>0 AND CATEGORIA_ID<6),
  CONSTRAINT FK_CATEGORIA FOREIGN KEY(CATEGORIA_ID) REFERENCES CATEGORIA(ID),
  CONSTRAINT PK_PRODUCTO PRIMARY KEY (ID)
);

CREATE TABLE TIPOCOMIDA(
   ID INTEGER NOT NULL,
   NOMBRE VARCHAR2(100 BYTE)NOT NULL,
   CONSTRAINT PK_TIPOCOMIDA PRIMARY KEY (ID)
);

CREATE TABLE PREFERENCIATIPO(
   PREFERENCIAS_ID INTEGER NOT NULL,
   TIPOCOMIDA_ID INTEGER NOT NULL,
   CONSTRAINT FK_PREFERENCIATIPO_PREF FOREIGN KEY (PREFERENCIAS_ID) REFERENCES PREFERENCIAS(ID),
   CONSTRAINT FK_PREFERENCIATIPO_TCOMIDA FOREIGN KEY (TIPOCOMIDA_ID) REFERENCES TIPOCOMIDA(ID),
   CONSTRAINT PK_PREFERENCIATIPO PRIMARY KEY (PREFERENCIAS_ID,TIPOCOMIDA_ID)
);

CREATE TABLE INGREDIENTE(
  ID INTEGER NOT NULL,
  NOMBRE VARCHAR2(100 BYTE) NOT NULL,
  DESCRIPCION VARCHAR2(1000 BYTE) NOT NULL,
  TRADUCCION VARCHAR2(1100 BYTE) NOT NULL,
  CONSTRAINT PK_INGREDIENTE PRIMARY KEY (ID)
);

CREATE TABLE PRODUCTOINGREDIENTE(
   PRODUCTO_ID INTEGER NOT NULL,
   INGREDIENTE_ID INTEGER NOT NULL,
   CONSTRAINT FK_PI_INGREDIENTE FOREIGN KEY (INGREDIENTE_ID) REFERENCES INGREDIENTE(ID),
   CONSTRAINT FK_PI_PRODUCTO FOREIGN KEY (PRODUCTO_ID) REFERENCES PRODUCTO(ID),
   CONSTRAINT PK_PRODUCTOINGREDIENTE PRIMARY KEY (PRODUCTO_ID,INGREDIENTE_ID)
);

CREATE TABLE ZONA(
   ID INTEGER NOT NULL,
   NOMBRE VARCHAR2(100) NOT NULL,
   CONSTRAINT PK_ZONA PRIMARY KEY (ID)
);

CREATE TABLE PREFERENCIASZONAS(
   PREFERENCIAS_ID INTEGER NOT NULL,
   ZONA_ID INTEGER NOT NULL,
   CONSTRAINT FK_PREFERENCIAZONA_PREF FOREIGN KEY (PREFERENCIAS_ID) REFERENCES PREFERENCIAS(ID),
   CONSTRAINT FK_PREFERENCIASZONA_ZONA FOREIGN KEY (ZONA_ID) REFERENCES ZONA(ID),
   CONSTRAINT PK_PREFERENCIASZONA PRIMARY KEY (PREFERENCIAS_ID,ZONA_ID)
);

CREATE TABLE RESERVA(
   ID INTEGER NOT NULL,
   FECHAHORA TIMESTAMP NOT NULL,
   DURACION INTEGER NOT NULL,
   COMENSALES INTEGER NOT NULL,
   NOMBRERESERVANTE VARCHAR2(100 BYTE) NOT NULL,
   TELEFONORESERVANTE INTEGER NOT NULL,
   ZONA_ID INTEGER NOT NULL,
   CONSTRAINT FK_RESERVA_ZONA FOREIGN KEY (ZONA_ID) REFERENCES ZONA(ID),
   CONSTRAINT PK_RESERVA PRIMARY KEY (ID)
);

CREATE TABLE ESPACIO(
    ID INTEGER NOT NULL,
    ACONDICIONAMIENTO VARCHAR2(1000 BYTE) NOT NULL,
    ABIERTO INTEGER NOT NULL,
    CAPACIDAD INTEGER NOT NULL,
    NECESIDADESESPECIALES INTEGER NOT NULL,
    CONDICIONESTECNICAS VARCHAR2(1500 BYTE) NOT NULL,
    ZONA_ID INTEGER,
    CONSTRAINT FK_ESPACIO_ZONA FOREIGN KEY (ZONA_ID) REFERENCES ZONA(ID),
    CONSTRAINT PK_ESPACIO PRIMARY KEY (ID)
);

CREATE TABLE RESTAURANTE(
  ID INTEGER NOT NULL,
  NOMBRE VARCHAR2(150 BYTE)NOT NULL,
  PAGINAWEB VARCHAR2(3999 BYTE),
  TIPOCOMIDA_ID NOT NULL, 
  REGISTRO_ID NOT NULL UNIQUE,
  ZONA_ID NOT NULL,
  CONSTRAINT FK_RESTAURANTE_ZONA FOREIGN KEY (ZONA_ID) REFERENCES ZONA(ID),
  CONSTRAINT FK_RESTAURANTE_REGISTRO FOREIGN KEY (REGISTRO_ID) REFERENCES REGISTRO(CODIGO),
  CONSTRAINT FK_RESTAURANTE_TIPOCOMIDA FOREIGN KEY (TIPOCOMIDA_ID) REFERENCES TIPOCOMIDA(ID),
  CONSTRAINT PK_RESTAURANTE PRIMARY KEY (ID)
);

CREATE TABLE REPRESENTANTE(
  ID INTEGER NOT NULL,
  NOMBRE VARCHAR2(500 BYTE) NOT NULL,
  TELEFONO INTEGER,
  CORREO VARCHAR2(100 BYTE),
  RESTAURANTE_ID INTEGER NOT NULL UNIQUE,
  CHECK (NOT(TELEFONO IS NULL AND CORREO IS NULL)),
  CONSTRAINT FK_REPRESENTANTE_RESTAURANTE FOREIGN KEY (RESTAURANTE_ID) REFERENCES RESTAURANTE(ID),
  CONSTRAINT PK_REPRESENTANTE PRIMARY KEY (ID)
);

CREATE TABLE MENU(
   ID INTEGER NOT NULL,
   CANTIDAD INTEGER NOT NULL,
   COSTODEPRODUCION NUMBER NOT NULL,
   VALORALPUBLICO NUMBER NOT NULL,
   PRODUCTOENTRADA_ID INTEGER,
   PRODUCTOPLATOFUERTE_ID INTEGER,
   PRODUCTOBEBIDA_ID INTEGER,
   PRODUCTOPOSTRE_ID INTEGER,
   PRODUCTOACOMPANAMIENTO_ID INTEGER,
   RESTAURANTE_ID INTEGER NOT NULL,
   CHECK (COSTODEPRODUCION>0 AND VALORALPUBLICO>=0),
   CONSTRAINT FK_MENU_PRODUCTOENTRADA FOREIGN KEY (PRODUCTOENTRADA_ID) REFERENCES PRODUCTO(ID),
   CONSTRAINT FK_MENU_PRODUCTOPLATOFUERTE FOREIGN KEY (PRODUCTOPLATOFUERTE_ID) REFERENCES PRODUCTO(ID),
   CONSTRAINT FK_MENU_PRODUCTOPOSTRE FOREIGN KEY (PRODUCTOPOSTRE_ID) REFERENCES PRODUCTO(ID),
   CONSTRAINT FK_MENU_PRODUCTOBEBIDA FOREIGN KEY (PRODUCTOBEBIDA_ID) REFERENCES PRODUCTO(ID),
   CONSTRAINT FK_MENU_PRODUCTOACOMPANAMIENTO FOREIGN KEY (PRODUCTOACOMPANAMIENTO_ID) REFERENCES PRODUCTO(ID),
   CONSTRAINT FK_MENU_RESTAURANTE FOREIGN KEY (RESTAURANTE_ID) REFERENCES RESTAURANTE(ID),
   CONSTRAINT PK_MENU PRIMARY KEY (ID)
);

CREATE TABLE SUBPEDIDO(
   MENU_ID INTEGER NOT NULL,
   PEDIDO_ID INTEGER NOT NULL,
   ANOTACION VARCHAR2(1500 BYTE),
   CONSTRAINT FK_SUBPEDIDO_MENU FOREIGN KEY (MENU_ID) REFERENCES MENU(ID),
   CONSTRAINT FK_SUBPEDIDO_PEDIDO FOREIGN KEY (PEDIDO_ID) REFERENCES PEDIDO(ID),
   CONSTRAINT PK_SUBPEDIDO PRIMARY KEY (MENU_ID,PEDIDO_ID)
);

CREATE TABLE MENURESERVA(
   MENU_ID INTEGER NOT NULL,
   RESERVA_ID INTEGER NOT NULL,
   CONSTRAINT FK_MENURESERVA_MENU FOREIGN KEY (MENU_ID) REFERENCES MENU(ID),
