   CONSTRAINT PK_MENURESERVA PRIMARY KEY (MENU_ID,RESERVA_ID)
);

CREATE TABLE MENUTIPOCOMIDA(
   MENU_ID INTEGER NOT NULL,
   TIPOCOMIDA_ID INTEGER NOT NULL,
   CONSTRAINT FK_MENUTIPOCOMIDA_MENU FOREIGN KEY (MENU_ID) REFERENCES MENU(ID),
